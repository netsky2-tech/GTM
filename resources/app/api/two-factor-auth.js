export default {
    enable2fa(data, cb, errorCb) {
        axios.post('auth/enable-2fa', data)
            .then(function (response) {
                if (response.data.status == 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },
    disable2fa(data, cb, errorCb) {
        axios.delete('auth/disabled-2fa', data)
        .then(function (response) {
            if (response.data.status === 'success') {
                cb(response.data.result)
            } else {
                errorCb(response.data.result)
            }
        })
        .catch(function (error) {
            errorCb(error)
        })
    },

    showQr(data, cb, errorCb) {
        axios.get('auth/show-qr', data)
            .then(function (response) {
                if (response.data.status == 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },
    getRecoveryCode(data, cb, errorCb) {
        axios.get('auth/get-recovery', data)
            .then(function (response) {
                if (response.data.status == 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            });
    },
    setRecoveryCode(data, cb, errorCb) {
        axios.get('auth/set-recovery-code', data)
            .then(function (response) {
                if (response.data.status == 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            });
    },
    storeTwoFactorCode(data, cb, errorCb) {
        axios.get('auth/store-two-factor-code', data)
            .then(function (response) {
                if (response.data.status === 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            });
    }
}
