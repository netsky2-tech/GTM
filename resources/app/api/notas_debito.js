export default {
    obtener(data, cb, errorCb) {
        axios.post('cuentas-cobrar/notas-debito/obtener', data)
        .then(function (response) {
            if (response.data.status == 'success') {
                cb(response.data.result)
            } else {
                errorCb(response.data.result)
            }
        })
        .catch(function (error) {
            errorCb(error)
        })
    },
    registrar(data, cb, errorCb) {
        axios.post('cuentas-cobrar/notas-debito/registrar', data)
        .then(function (response) {
            if (response.data.status == 'success') {
                cb(response.data.result)
            } else {
                errorCb(response.data.result)
            }
        })
        .catch(function (error) {
            errorCb(error)
        })
    },

    nueva(data, cb, errorCb) {
        axios.post('cuentas-cobrar/notas-debito/nueva', data)
            .then(function (response) {
                if (response.data.status == 'success') {
                    cb(response.data.result)
                } else {
                    errorCb(response.data.result)
                }
            })
            .catch(function (error) {
                errorCb(error)
            })
    },
    obtenerNotaDebito(data, cb, errorCb) {
        axios.post('cuentas-cobrar/notas-debito/obtener-nota-debito', data)
        .then(function (response) {
            if (response.data.status == 'success') {
                cb(response.data.result)
            } else {
                errorCb(response.data.result)
            }
        })
        .catch(function (error) {
            errorCb(error)
        })
    },
}